let nome = 'Alexandre';

switch (nome) {
  case 'Alexandre':
    console.log('O nome é Alexandre');
    break;
  case 'João':
    console.log('O nome é João');
    break;
  default:
    console.log('Nome não encontrado');
}

if (nome == 'Alexandre') {
  console.log('O nome é Alexandre');
} else if (nome == 'João') {
  console.log('O nome é João');
} else {
  console.log('Nome não encontrado');
}
